function changeLanguage(selectedLanguage) {
    const texts = translations[selectedLanguage];

    for (const key in texts) {
        const elements = document.querySelectorAll(`[data-key="${key}"]`);
        elements.forEach(element => {
            element.textContent = texts[key];
        });
    }

    // Uložení vybraného jazyka do localStorage
    localStorage.setItem('language', selectedLanguage);
}

document.addEventListener('DOMContentLoaded', () => {
    // Načtení jazyka z localStorage, pokud je uložený
    const userLang = localStorage.getItem('language') || 'cs'; // Výchozí jazyk
    changeLanguage(userLang);
});

// Funkce pro změnu jazyka (např. při kliknutí na tlačítko pro změnu jazyka)
function setLanguage(lang) {
    changeLanguage(lang);
}